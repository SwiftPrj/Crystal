using System;

namespace Crystal.Tokenization
{
    public class Token
    {
        public TokenType Type { get; }
        public string Value { get; }

        public Token(TokenType type, string value)
        {
            Type = type;
            Value = value;
        }

        public override string ToString()
        {
            return $"Token= {Type}: {Value}";
        }
    }
}
